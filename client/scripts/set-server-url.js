const fs = require('fs');
const path = require('path');

const url = process.env.LURA_SERVER_URL || 'http://localhost:3000';
const configPath = path.join(__dirname, '..', 'src', 'config.js');

const content = `// This file is auto-generated by the build script.
// Override by setting LURA_SERVER_URL env variable before building.
module.exports = {
  defaultServerUrl: '${url}',
};
`;

fs.writeFileSync(configPath, content);
console.log(`[config] Server URL set to: ${url}`);
